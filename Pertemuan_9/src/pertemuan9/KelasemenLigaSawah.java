    private boolean isKlubExists(String klub) {
        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);
            String sql = "SELECT COUNT(*) FROM LigaSawah WHERE LOWER(Klub) = LOWER(?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, klub);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                return rs.getInt(1) > 0;
            }

            rs.close();
            pst.close();
            conn.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error checking klub: " + ex.getMessage());
        }
        return false;
    }


