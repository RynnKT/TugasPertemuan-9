    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        try {
            if (NomorPendaftaran.getText().trim().isEmpty()
                    || Klub.getText().trim().isEmpty()
                    || AsalKlub.getText().trim().isEmpty()
                    || Point.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
                return;
            }

            // Cek apakah nomor sudah ada
            if (isNomorExists(Integer.parseInt(NomorPendaftaran.getText()))) {
                JOptionPane.showMessageDialog(this, "Nomor pendaftaran sudah ada! Gunakan nomor lain.");
                return;
            }

            // Cek apakah klub sudah ada
            if (isKlubExists(Klub.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Nama klub sudah ada! Gunakan nama klub lain.");
                return;
            }

            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);
            String sql = "INSERT INTO LigaSawah (Nomor, Klub, KotaAsal, PointKemenangan) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(NomorPendaftaran.getText()));
            pst.setString(2, Klub.getText());
            pst.setString(3, AsalKlub.getText());
            pst.setInt(4, Integer.parseInt(Point.getText()));

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");

            // Refresh tabel
            TampilkanTabel();
            resetInsertFields();
            // Tutup dialog insert
            InsertDialog.setVisible(false);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal tambah data: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_SimpanActionPerformed

